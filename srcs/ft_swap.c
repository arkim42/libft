#include "libft.h"

void				ft_swap(void *a, void *b, size_t n)
{
	void		*t;

	t = malloc(n);
	ft_memcpy(t, a, n);
	ft_memcpy(a, b, n);
	ft_memcpy(b, t, n);
	free(t);
}
